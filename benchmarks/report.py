"""Render eval-harness results as a stdout/Markdown table and a JSON artifact."""

from __future__ import annotations

import json
from collections.abc import Sequence
from typing import Any

from benchmarks.corpus import Corpus
from benchmarks.harness import (
    OperatingPoint,
    PresetResult,
    Scored,
    ThresholdResult,
    perturbation_recall,
)


def _threshold_table(results: Sequence[ThresholdResult]) -> list[str]:
    lines = [
        "| Threshold | TP | FP | TN | FN | Precision | Recall | FPR | F1 |",
        "| --- | --- | --- | --- | --- | --- | --- | --- | --- |",
    ]
    for r in results:
        lines.append(
            f"| {r.threshold:.4f} | {r.tp} | {r.fp} | {r.tn} | {r.fn} | "
            f"{r.precision:.3f} | {r.recall:.3f} | {r.fpr:.3f} | {r.f1:.3f} |"
        )
    return lines


def _preset_table(presets: Sequence[PresetResult]) -> list[str]:
    lines = [
        "| Preset | Match thr | Ambig ceiling | TP | FP | TN | FN | Precision | Recall | FPR |",
        "| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |",
    ]
    for p in presets:
        r = p.flagged
        lines.append(
            f"| {p.name} | {p.match_threshold:.3f} | {p.ambiguous_ceiling:.3f} | "
            f"{r.tp} | {r.fp} | {r.tn} | {r.fn} | "
            f"{r.precision:.3f} | {r.recall:.3f} | {r.fpr:.3f} |"
        )
    return lines


def _perturbation_table(
    scored: Sequence[Scored], threshold: float, order: Sequence[str]
) -> list[str]:
    recall = perturbation_recall(scored, threshold)
    lines = [
        f"Per-perturbation recall at the recommended threshold ({threshold:.4f}):",
        "",
        "| Perturbation | Caught | Total | Recall |",
        "| --- | --- | --- | --- |",
    ]
    seen = set()
    for kind in order:
        if kind not in recall:
            continue
        seen.add(kind)
        caught, total = recall[kind]
        lines.append(f"| {kind} | {caught} | {total} | {caught / total:.3f} |")
    for kind in sorted(set(recall) - seen):
        caught, total = recall[kind]
        lines.append(f"| {kind} | {caught} | {total} | {caught / total:.3f} |")
    return lines


def render_markdown(
    corpus: Corpus,
    scored: Sequence[Scored],
    sweep: Sequence[ThresholdResult],
    presets: Sequence[PresetResult],
    operating_point: OperatingPoint,
    perturbation_order: Sequence[str],
) -> str:
    """Render the full human-readable report as Markdown."""
    n_scam = sum(1 for s in scored if s.image.is_scam)
    n_clean = sum(1 for s in scored if not s.image.is_scam)
    op = operating_point.result
    lines = [
        "# Detection evaluation",
        "",
        "Generated by `python -m benchmarks` against a deterministic synthetic",
        "corpus. The index is seeded with each scam campaign's base image; scam",
        "variants are perturbed re-shares that should still match, and clean",
        "images should not. An image is *flagged* when its best ensemble score",
        "(lower = closer match) is at or below the threshold.",
        "",
        f"Corpus: {corpus.num_campaigns} campaigns, {n_scam} scam images "
        f"({len(corpus.bases)} bases + {len(corpus.variants)} variants), "
        f"{n_clean} clean negatives.",
        "",
        "## Recommended operating point",
        "",
        f"- **Score threshold:** {op.threshold:.4f}",
        f"- **Precision / Recall / F1:** {op.precision:.3f} / {op.recall:.3f} / {op.f1:.3f}",
        f"- **FPR:** {op.fpr:.3f}  (FP={op.fp}, on {n_clean} clean images)",
        f"- **Rationale:** {operating_point.rationale}",
        "",
        "## Shipped sensitivity presets",
        "",
        "The production matcher flags both SCAM and AMBIGUOUS verdicts, so the",
        "effective operating threshold per preset is its *ambiguous ceiling*.",
        "",
        *_preset_table(presets),
        "",
        "## Threshold sweep",
        "",
        *_threshold_table(sweep),
        "",
        "## Per-perturbation recall",
        "",
        *_perturbation_table(scored, op.threshold, perturbation_order),
        "",
    ]
    return "\n".join(lines)


def build_json(
    corpus: Corpus,
    scored: Sequence[Scored],
    sweep: Sequence[ThresholdResult],
    presets: Sequence[PresetResult],
    operating_point: OperatingPoint,
) -> dict[str, Any]:
    """Build a JSON-serializable summary of the run."""
    op = operating_point.result
    return {
        "corpus": {
            "campaigns": corpus.num_campaigns,
            "bases": len(corpus.bases),
            "variants": len(corpus.variants),
            "cleans": len(corpus.cleans),
        },
        "operating_point": {
            "threshold": op.threshold,
            "precision": op.precision,
            "recall": op.recall,
            "f1": op.f1,
            "fpr": op.fpr,
            "tp": op.tp,
            "fp": op.fp,
            "tn": op.tn,
            "fn": op.fn,
            "rationale": operating_point.rationale,
        },
        "presets": [
            {
                "name": p.name,
                "match_threshold": p.match_threshold,
                "ambiguous_ceiling": p.ambiguous_ceiling,
                "tp": p.flagged.tp,
                "fp": p.flagged.fp,
                "tn": p.flagged.tn,
                "fn": p.flagged.fn,
                "precision": p.flagged.precision,
                "recall": p.flagged.recall,
                "fpr": p.flagged.fpr,
                "f1": p.flagged.f1,
            }
            for p in presets
        ],
        "sweep": [
            {
                "threshold": r.threshold,
                "tp": r.tp,
                "fp": r.fp,
                "tn": r.tn,
                "fn": r.fn,
                "precision": r.precision,
                "recall": r.recall,
                "fpr": r.fpr,
                "f1": r.f1,
            }
            for r in sweep
        ],
        "perturbation_recall": {
            kind: {"caught": caught, "total": total, "recall": caught / total}
            for kind, (caught, total) in perturbation_recall(
                scored, operating_point.result.threshold
            ).items()
        },
    }


def write_json(path: str, payload: dict[str, Any]) -> None:
    """Write ``payload`` to ``path`` as pretty-printed, sorted JSON."""
    with open(path, "w", encoding="utf-8") as fh:
        json.dump(payload, fh, indent=2, sort_keys=True)
        fh.write("\n")
